// Function to check even or odd
function checkEvenOrOdd(number) {
    if (number % 2 === 0) {
        console.log(`${number} is Even`);
    } else {
        console.log(`${number} is Odd`);
    }
}

// Example usage
checkEvenOrOdd(10); // Output: 10 is Even
checkEvenOrOdd(7);  // Output: 7 is Odd


